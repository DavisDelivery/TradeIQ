// netlify/functions/shared/universe-history.ts
//
// Point-in-time historical index membership.
//
// Phase 4's backtest is exposed to survivorship bias if its universe is
// "current S&P 500 constituents" — the companies that got delisted,
// acquired, or dropped from the index don't appear, and they're
// disproportionately the losers. This module answers questions like
// "was AAPL in the S&P 500 on 2018-03-15?" deterministically.
//
// Granularity: month-end snapshots. Sufficient for the quarterly /
// monthly rebalance frequencies TradeIQ actually backtests. Daily
// resolution is a six-figure data product.
//
// Source maintenance: the static UNIVERSE_HISTORY array below is
// generated by `scripts/generate-universe-history.ts`. See
// `docs/UNIVERSE_HISTORY_RUNBOOK.md` for refresh instructions.
//
// Coverage status (audit-time):
//   - Dow:     2018-01-31 → 2026-04-30, monthly. 100+ snapshots,
//              hand-curated against publicly documented index changes.
//   - SP500:   2026-04-30 only (current seed). Needs generator-script
//              run from a non-restricted env to extend.
//   - NDX:     2026-04-30 only (current seed). Same as SP500.
//   - Russell2k: 2026-04-30 only (current seed). iShares CSV scrape
//                required for historical depth.
//
// Phase 3 ships the framework correct and Dow at full coverage; SP500 /
// NDX / Russell history extension is a runbook task in any non-egress-
// restricted environment.
//
// PIT-cacheable: every lookup is a pure function of (index, date).

import { UNIVERSE } from './universe';

export type UniverseIndex = 'sp500' | 'ndx' | 'dow' | 'russell2k';

export interface UniverseSnapshot {
  /** YYYY-MM-DD month-end. */
  date: string;
  index: UniverseIndex;
  /** Sorted alphabetically for deterministic comparison. */
  tickers: string[];
}

